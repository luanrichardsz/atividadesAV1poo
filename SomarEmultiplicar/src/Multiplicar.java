public class Multiplicar {
    public static void Multiplicar(int A, int B, int C, int D){
        int multi1 = A * B;
        int multi2 = A * C;
        int multi3 = A * D;
        int multi4 = B * C;
        int multi5 = B * D;
        int multi6 = C * D;

        System.out.println("\nA * B = " + multi1);
        System.out.println("A * C = " + multi2);
        System.out.println("A * D = " + multi3);
        System.out.println("B * C = " + multi4);
        System.out.println("B * D = " + multi5);
        System.out.println("C * D = " + multi6);
    }
}
